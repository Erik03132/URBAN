class House: # создаем класс House
    def __init__(self, name, number_of_floors): # метод __init__ вызывается при создании объекта
        self.name = name # self указывает на текущий объект, присваиваем его атрибуту name переданное значение
        self.number_of_floors = number_of_floors # self указывает на текущий объект, присваиваем его атрибуту number_of_floors переданное значение

    def go_to(self, new_floor): # создаем метод go_to с параметром new_floor
        if 1 <= new_floor <= self.number_of_floors: # используем self для обращения к атрибуту number_of_floors текущего объекта
            for floor in range(1, new_floor + 1): # перебираем этажи от 1 до new_floor включительно
                print(floor) # выводим текущий этаж на консоль
        else: # если новый этаж вне допустимого диапазона
            print("Такого этажа не существует") # выводим сообщение об ошибке

# создаем объект класса House с произвольным названием и количеством этажей
house = House('ЖК Эльбрус', 30) # метод __init__ вызывается автоматически

# вызываем метод go_to у созданного объекта с произвольным числом
house.go_to(5) # обращаемся к методу go_to объекта house, выводит числа от 1 до 5 включительно

